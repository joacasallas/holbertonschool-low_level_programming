#include "main2.h"

/**
 * add - sum 2 integers.
 * @a: first integer
 * @b: second integer
 * Return: result of the operation
 */

int add(int a, int b)
{
    return (a + b);
}
/**
 * sub - sub 2 integers.
 * @a: first integer
 * @b: second integer
 * Return: result of the operation
 */

int sub(int a, int b)
{
    return (a - b);
}

/**
 * mul - mul 2 integers.
 * @a: first integer
 * @b: second integer
 * Return: result of the operation
 */

int mul(int a, int b)
{
    return (a * b);
}

/**
 * div - div 2 integers.
 * @a: first integer
 * @b: second integer
 * Return: result of the operation
 */

int div(int a, int b)
{
    return (a / b);
}

/**
 * mod - mod 2 integers.
 * @a: first integer
 * @b: second integer
 * Return: result of the operation
 */

int mod(int a, int b)
{
    return (a % b);
}